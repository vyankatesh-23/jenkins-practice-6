def add(a, b):
    return a + b

def sub(a, b):
    return a - b

def multi(a, b):
    return a * b

def divide(a, b):
    return a / b

if __name__ == "__main__":
    print(f"Addition: {add(10, 5)}")
    print(f"subtraction: {sub(10, 5)}")
    print(f"multiplication: {multi(10, 5)}")
    print(f"division: {divide(10, 5)}")

